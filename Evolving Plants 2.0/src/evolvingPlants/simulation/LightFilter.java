package evolvingPlants.simulation;

public class LightFilter
	{
		public int x, y, width, height, red, green, blue;

		public LightFilter(int x, int y, int width, int height, int red, int green, int blue)
			{
				this.x = x;
				this.y = y;
				this.width = width;
				this.height = height;
				this.red = red;
				this.green = red;
				this.blue = red;
			}
	}
